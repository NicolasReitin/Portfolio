        Notre histoire & concept
        
        Lancé en 2015 à Bordeaux, Ma Ville Mon Shopping est une start-up qui fait le lien entre des shoppeurs passionnés, des boutiques indépendantes et des clients désireux d’acheter des produits de qualité.
        
        Les équipes de Ma Ville Mon Shopping parcourent la ville et sélectionnent par catégorie des boutiques revendeuses des meilleurs produits du moment.
        
        mavillemonshopping.fr offre aux magasins la possibilité d’apporter de nouveaux services à leurs clients en leur mettant à disposition des services de livraison, de click and collect et de réservation.
        Notre philosophie
        
        Convaincu qu’il est nécessaire de conserver une part d’humanité dans l’acte d’achat, Ma Ville Mon Shopping souhaite aider les boutiques physiques à répondre aux nouvelles attentes des clients et souhaite favoriser le concept d’achat positif :
        
        Tout d’abord parce que Ma Ville Mon Shopping est + local : en achetant sur Ma Ville Mon Shopping, vous favorisez l’activité économique de votre ville et participez au maintien des boutiques et des emplois ;
        
        Deuxièmement parce que Ma Ville Mon Shopping est + responsable : en utilisant Ma Ville Mon Shopping vous achetez dans une boutique proche de chez vous, favorisez les circuits courts et limitez l’impact environnemental lié à l’acheminement de votre produit;
        
        Enfin parce que Ma Ville Mon Shopping est + humain : Ma Ville Mon Shopping vit au travers des femmes et des hommes passionnés et engagés qui font le choix de consommer mieux et autrement en conservant une relation humaine de proximité.
        
